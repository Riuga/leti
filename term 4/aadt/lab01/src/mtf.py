def mtf_encode(data: bytes) -> bytes:
    """
    Преобразование Move-to-Front (MTF).
    Идея: заменить каждый байт его индексом в динамическом списке символов (алфавите).
    После каждого использования символ перемещается в начало списка.
    В результате часто встречающиеся символы получают маленькие индексы (0, 1, 2...),
    что улучшает сжатие последующими алгоритмами (например, RLE или Хаффманом).
    Алгоритм:
        1. Инициализируем алфавит — список из 256 байт от 0 до 255.
        2. Для каждого байта входных данных:
            a) Находим его позицию (индекс) в текущем алфавите.
            b) Сохраняем этот индекс в выходной массив.
            c) Удаляем байт из алфавита и вставляем его в начало.
        3. Возвращаем последовательность индексов как байты (каждый индекс от 0 до 255).
    Аргументы:
        data: исходные байты
    Возвращает:
        байты, где каждый байт — индекс в динамическом алфавите
    """
    alphabet = bytearray(range(256))
    encoded = bytearray()
    for byte in data:
        # находим позицию текущего байта
        index = alphabet.index(byte)
        # сохраняем индекс
        encoded.append(index)
        # перемещаем байт в начало
        del alphabet[index]
        alphabet.insert(0, byte)
    return bytes(encoded)


def mtf_decode(encoded_data: bytes) -> bytes:
    """
    Обратное преобразование Move-to-Front (MTF).
    Восстанавливает исходные данные из последовательности индексов,
    полученной после mtf_encode. Использует тот же принцип обновления алфавита.
    Алгоритм:
        1. Инициализируем алфавит — список из 256 байт от 0 до 255.
        2. Для каждого индекса из входных данных:
            a) Берём байт из алфавита по этому индексу.
            b) Добавляем байт в результат.
            c) Удаляем байт из алфавита и вставляем его в начало.
        3. Возвращаем восстановленные байты.
    Аргументы:
        encoded_data: байты, полученные из mtf_encode (индексы)
    Возвращает:
        исходные байты
    """
    alphabet = bytearray(range(256))
    decoded = bytearray()
    for index in encoded_data:
        # получаем байт по индексу
        byte = alphabet[index]
        decoded.append(byte)
        del alphabet[index]
        alphabet.insert(0, byte)
    return bytes(decoded)
